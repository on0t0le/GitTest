package ua.on0t0le;

public class Car {
	
		private String Model;
		private String Name;
		
		public Car(String Name, String Model){
			this.Name=Name;
			this.Model=Model;
		}
		public String getModel() {
			return Model;
		}
		public void setModel(String model) {
			Model = model;
		}
		public String getName() {
			return Name;
		}
		public void setName(String name) {
			Name = name;
		}
		
		
		

	

}
